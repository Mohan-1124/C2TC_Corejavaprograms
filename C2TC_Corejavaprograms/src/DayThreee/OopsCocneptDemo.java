package DayThreee;

public class OopsCocneptDemo {
	private int serialNum;
	private int age;
	private String name;
	public int getSerialNum() {
		return serialNum;
	}
	public void setSerialNum(int serialNum) {
		this.serialNum = serialNum;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	@Override
	public String toString() {
		return "OopsCocneptDemo [serialNum=" + serialNum + ", age=" + age + ", name=" + name + "]";
	}
	
	

}
